public class Station {
   
   private String name;
   private int id;
   
   public Station(String name) {
	   this.name = name;
   }
   
   public void setName(String value) {
      this.name = value;
   }
   
   public String getName() {
      return this.name;
   }

   public int getID() {
      return id;
   }

   public void setID(int id) {
      this.id = id;
   }
   
   }
